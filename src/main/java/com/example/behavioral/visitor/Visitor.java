package com.example.behavioral.visitor;

/**
 * 访问者接口（Visitor）
 * 
 * 访问者模式中的访问者接口，定义了对不同元素类型的访问方法
 * 每个方法对应一种元素类型，访问者可以根据元素类型执行不同的操作
 * 
 * 访问者模式的核心思想：
 * - 将操作从元素类中分离出来，封装在访问者中
 * - 通过双重分发机制，实现元素和访问者的动态绑定
 * - 添加新的操作只需要添加新的访问者类，无需修改元素类
 */
public interface Visitor {
    
    /**
     * 访问猫
     * 
     * 当访问者访问 Cat 对象时，会调用此方法
     * 具体的操作逻辑在实现类中定义
     * 
     * @param cat 被访问的猫对象
     */
    void visit(Cat cat);

    /**
     * 访问狗
     * 
     * 当访问者访问 Dog 对象时，会调用此方法
     * 具体的操作逻辑在实现类中定义
     * 
     * @param dog 被访问的狗对象
     */
    void visit(Dog dog);
}
